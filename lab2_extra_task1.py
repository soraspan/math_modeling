a = int(input('Введите коэффицент a: '))
b = int(input('Введите коэффицент b: '))
c = int(input('Введите коэффицент c: '))

dis = b**2 - 4 * a * c

if dis > 0:
  print(f'Первый корень: {(-b + dis**0.5) / 2 * a}\nВторой корень: {(-b - dis**0.5) / 2 * a}')
elif dis == 0:
  print(f'Корни равны: {(-b + dis**0.5) / 2 * a}, {-((-b + dis**0.5) / 2 * a)}')
else:
  print('Нет корней')